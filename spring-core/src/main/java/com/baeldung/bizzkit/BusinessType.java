package com.baeldung.bizzkit;

public class BusinessType {

    private long id;

    private String name;

    public BusinessType() {
    }

    public long getId() {
        return id;
    }

    public void setId(long id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

}
