package com.baeldung.mongodb.events;

import java.math.BigInteger;

import org.springframework.beans.factory.annotation.Autowired;
import org.springframework.data.mongodb.core.mapping.event.AbstractMongoEventListener;
import org.springframework.data.mongodb.core.mapping.event.BeforeConvertEvent;
import org.springframework.stereotype.Component;

import com.baeldung.mongodb.models.UserAutoGeneratedField;
import com.baeldung.mongodb.services.SequenceGeneratorService;


@Component
public class UserModelListener extends AbstractMongoEventListener<UserAutoGeneratedField> {

    private SequenceGeneratorService sequenceGenerator;

    @Autowired
    public UserModelListener(SequenceGeneratorService sequenceGenerator) {
        this.sequenceGenerator = sequenceGenerator;
    }

    @Override
    public void onBeforeConvert(BeforeConvertEvent<UserAutoGeneratedField> event) {
        if (event.getSource().getId().intValue() < 1) {
            event.getSource().setId(BigInteger.valueOf(sequenceGenerator.generateSequence(UserAutoGeneratedField.SEQUENCE_NAME)));
        }
    }


}
