package com.baeldung.resttemplateexception.model;

public class Criterion {

    private String prop;
    private String value;

    public Criterion() {

    }

    public String getProp() {
        return prop;
    }

    public void setProp(String prop) {
        this.prop = prop;
    }

    public String getValue() {
        return value;
    }

    public void setValue(String value) {
        this.value = value;
    }

}
