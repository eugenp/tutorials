package com.baeldung.thymeleaf.enums;

public class Widget {
    private String name;
    private Color color;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public Color getColor() {
        return color;
    }

    public void setColor(Color color) {
        this.color = color;
    }

    @Override
    public String toString() {
        return "Widget [name=" + name + ", color=" + color + "]";
    }
}
