package com.baeldung.image;

public class CarColorCount {

    private String color;
    private int count;

    public CarColorCount() {
    }

    public String getColor() {
        return color;
    }

    public void setColor(String color) {
        this.color = color;
    }

    public int getCount() {
        return count;
    }

    public void setCount(int count) {
        this.count = count;
    }

}
