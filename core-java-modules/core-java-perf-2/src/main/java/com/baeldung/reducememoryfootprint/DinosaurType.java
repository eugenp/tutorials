package com.baeldung.reducememoryfootprint;

public class DinosaurType {

    String kingdom;
    String phylum;
    String clazz;
    String order;
    String family;
    String genus;
    String species;

    public DinosaurType(String kingdom, String phylum, String clazz, String order, String family, String genus, String species) {
        this.kingdom = kingdom;
        this.phylum = phylum;
        this.clazz = clazz;
        this.order = order;
        this.family = family;
        this.genus = genus;
        this.species = species;
    }
}
