package com.baeldung.java.copy;

/**
* Industries that Companies can engage in.
*/
public class Industry {

    /** The name of the Industry. */
    private String name;

    public Industry(String name) {
        this.name = name;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

}
