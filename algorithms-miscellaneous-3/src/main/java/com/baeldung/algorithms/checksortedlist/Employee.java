package com.baeldung.algorithms.checksortedlist;

public class Employee {

    long id;

    String name;

    public Employee() {
    }

    public Employee(long id, String name) {
        super();
        this.id = id;
        this.name = name;
    }

    public long getId() {
        return id;
    }

    public void setId(long id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

}
